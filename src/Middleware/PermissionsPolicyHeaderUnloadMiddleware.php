namespace App\Middleware;

use Psr\Http\Message\ResponseInterface;
use Psr\Http\Message\ServerRequestInterface;
use Psr\Http\Server\MiddlewareInterface;
use Psr\Http\Server\RequestHandlerInterface;

class PermissionsPolicyHeaderUnloadMiddleware implements MiddlewareInterface
{
    /**
     * Adds a "Permissions-Policy" header in order to allows the "unload" window event on web clients.
     * This is a temporary fix (PB-50644) to ensure that Chrome displays the browser extension pages as expected without using BFCaching
     *
     * @param \Psr\Http\Message\ServerRequestInterface $request The request.
     * @param \Psr\Http\Server\RequestHandlerInterface $handler The request handler.
     * @return \Psr\Http\Message\ResponseInterface A response.
     */
    public function process(ServerRequestInterface $request, RequestHandlerInterface $handler): ResponseInterface
    {
        $response = $handler->handle($request);
        $response = $response->withHeader('Permissions-Policy', 'unload=(*)');

        return $response;
    }
}
